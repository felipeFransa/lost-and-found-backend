package br.com.felipe.model;

public class Finds {
    private String animalsType;
    private String animalsName;
    private String region;

    public String getAnimalsType() {
        return animalsType;
    }

    public void setAnimalsType(String animalsType) {
        this.animalsType = animalsType;
    }

    public String getAnimalsName() {
        return animalsName;
    }

    public void setAnimalsName(String animalsName) {
        this.animalsName = animalsName;
    }

    public String getRegion() {
        return region;
    }

    public void setRegion(String region) {
        this.region = region;
    }
}
